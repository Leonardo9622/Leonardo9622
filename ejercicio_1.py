# Constante 
porcentaje = 0.66 

# Entrada 
cantidad = float ( input ( "Ingresa la cantidad: " ) ) 

# Proceso 
aumento = cantidad + ( cantidad * porcentaje ) 

# Salida 
print ("El resultado es igual a: ", aumento ) 